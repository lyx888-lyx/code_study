<script>
import { addInformation } from '../../../api/api';
export default {
  name: "Information",
  data() {
    return {
      tableData: [{
        date: '2016-05-02',
        name: '王小虎',
        address: '上海市普陀区金沙江路 1518 弄'
      }, {
