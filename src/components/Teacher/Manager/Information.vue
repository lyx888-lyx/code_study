          </el-form-item>
        </el-form>
        <div slot="footer" class="dialog-footer">
          <el-button @click="dialogFormVisible = false">取 消</el-button>
          <el-button type="primary" @click="yesAddInformation">确 定</el-button>
        </div>
      </el-dialog>
    </el-card>
  </div>
